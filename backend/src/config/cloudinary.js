import { v2 as cloudinary } from "cloudinary";
import { CloudinaryStorage } from "multer-storage-cloudinary";
import dotenv from "dotenv";

dotenv.config();

cloudinary.config({
  cloud_name: process.env.CLOUDINARY_CLOUD_NAME,
  api_key: process.env.CLOUDINARY_API_KEY,
  api_secret: process.env.CLOUDINARY_API_SECRET,
});

//set up the cloudinary storage engines
const storage = new CloudinaryStorage({
  cloudinary: cloudinary,
  params: async (req, file) => {
    let folderName = "gebeta/general";
    if (req.originalUrl.includes("user")) folderName = "gebeta/avatars";
    if (req.originalUrl.includes("business")) folderName = "gebeta/businesses";
    if (req.originalUrl.includes("menu")) folderName = "gebeta/menu_items";

    return {
      folder: folderName,
      allowed_formats: ["jpg", "jpeg", "png", "gif", "webp"],
      transformation: [{ width: 800, height: 800, crop: "limit" }],
    };
  },
});

export {cloudinary, storage}; 
